package com.vzw.model;

public class TechSpecVO {
	private String title;
	private String imageUrl;
	private String techSpecFlag;
	private String detailedText;
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getImageUrl() {
		return imageUrl;
	}
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	public String getTechSpecFlag() {
		return techSpecFlag;
	}
	public void setTechSpecFlag(String techSpecFlag) {
		this.techSpecFlag = techSpecFlag;
	}
	public String getDetailedText() {
		return detailedText;
	}
	public void setDetailedText(String detailedText) {
		this.detailedText = detailedText;
	}

}
